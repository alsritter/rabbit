package helpers

// MaxInt32 返回最大值
func MaxInt32(x, y int32) int32 {
	if x > y {
		return x
	}
	return y
}

// MaxInt 返回最大值
func MaxInt(x, y int) int {
	if x > y {
		return x
	}
	return y
}

// Maxint32 返回最大值
func Maxint32(x, y int32) int32 {
	if x > y {
		return x
	}
	return y
}

// MinInt32 返回最小值
func MinInt32(x, y int32) int32 {
	if x < y {
		return x
	}
	return y
}

// MinInt 返回最小值
func MinInt(x, y int) int {
	if x < y {
		return x
	}
	return y
}

// Minint32 返回最小值
func Minint32(x, y int32) int32 {
	if x < y {
		return x
	}
	return y
}
